fn f(x: f64) -> f64 {
    // Taylor series expansion of  f(x) = sqrt(1+x)-1
    x / 2.0 - x.powf(2.0) / 8.0 + x.powf(3.0) / 16.0 - (5.0 * x.powf(4.0)) / 128.0
        + (7.0 * x.powf(5.0) / 256.0)
        - (21.0 * x.powf(6.0) / 1024.0)
        + (33.0 * x.powf(7.0) / 2048.0)
        - (429.0 * x.powf(8.0) / 32768.0)
        + (715.0 * x.powf(9.0) / 65536.0)
        - (2431.0 * x.powf(10.0) / 262144.0)
        + (4199.0 * x.powf(11.0) / 524288.0)
}
