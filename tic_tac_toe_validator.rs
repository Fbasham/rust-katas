fn is_solved(a: &[&[u8; 3]; 3]) -> i8 {
    let v = vec![
        format!("{}{}{}", &a[0][0], &a[0][1], &a[0][2]),
        format!("{}{}{}", &a[1][0], &a[1][1], &a[1][2]),
        format!("{}{}{}", &a[2][0], &a[2][1], &a[2][2]),
        format!("{}{}{}", &a[0][0], &a[1][0], &a[2][0]),
        format!("{}{}{}", &a[0][1], &a[1][1], &a[2][1]),
        format!("{}{}{}", &a[0][2], &a[1][2], &a[2][2]),
        format!("{}{}{}", &a[0][0], &a[1][1], &a[2][2]),
        format!("{}{}{}", &a[2][0], &a[1][1], &a[0][2]),
    ];
    if v.iter().any(|e| e == "111") {
        return 1;
    }
    if v.iter().any(|e| e == "222") {
        return 2;
    }
    if v.iter().filter(|&e| !e.contains("0")).count() == 8 {
        return 0;
    }
    -1
}
